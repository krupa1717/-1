def chebyshev(n, a, b):
    if n not in cheb_uzli:
        raise ValueError(f"Чебышёв не существует для n={n}. Допустимые n: {list(cheb_uzli.keys())}")
    uzli = cheb_uzli[n]
    w = [(b - a) / n] * n
    half_len = (b - a) / 2
    mid = (a + b) / 2
    x = [mid + half_len * t for t in uzli]
    return sum(w[i] * f(x[i]) for i in range(n))
